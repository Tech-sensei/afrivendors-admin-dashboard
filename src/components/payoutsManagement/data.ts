export type PayoutStatus =
  | "Pending"
  | "Processing"
  | "Approved"
  | "Completed"
  | "Rejected"
  | "Failed"
export type KycStatus = "Verified" | "Pending"

export interface PayoutHistoryItem {
  id: string
  date: string
  amount: number
  type: "Credit" | "Debit"
  description: string
}

export interface PayoutRequest {
  id: string
  vendorId: string
  vendorName: string
  vendorOwner: string
  vendorEmail: string
  vendorPhone: string
  amount: number
  bankName: string
  accountNumber: string
  accountName: string
  dateRequested: string
  timeRequested: string
  status: PayoutStatus
  requestedBy: string
  walletBalance: number
  completedBookings: number
  platformFee: number
  netAmount: number
  transactionHistory: PayoutHistoryItem[]
  kycStatus: KycStatus
  lastPayout?: string
  lastPayoutAmount?: number
  approvedBy?: string
  approvedDate?: string
  approvedTime?: string
  payoutDate?: string
  payoutRef?: string
  rejectedBy?: string
  rejectedDate?: string
  rejectedTime?: string
  rejectionReason?: string
  /** Admin API: wallet transaction id */
  transactionId?: string | null
  transactionRef?: string | null
  /** From GET /admin/payouts/:id ledger */
  payoutCurrency?: string | null
  ledgerDescription?: string | null
  ledgerType?: string | null
  ledgerReferenceId?: string | null
  ledgerReferenceType?: string | null
  vendorCategory?: string | null
  stripeConnectStatus?: string | null
}

